class UserRequest {
  String username;

  String firstName;

  String lastName;

  String email;

  String password;

  String rePassword;

  String phone;

  UserRequest({
    required this.username,
    required this.firstName,
    required this.lastName,
    required this.email,
    required this.password,
    required this.rePassword,
    required this.phone,
  });
}
